
#Мета: Створити програму, яка приймає від користувача послідовні числа до тих пір,
# поки він не введе спеціальне значення (наприклад, -1).
# Після цього вивести середнє арифметичне введених чисел.

sum = 0
count = 0
number = 0
while number != -1:
    number = input("Введіть число (-1 для завершення): ")

    if number == -1:
        break


sum += float(number)
count += 1
